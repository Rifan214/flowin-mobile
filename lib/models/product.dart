class Product {
  final String name;
  final String image;
  final String tag;
  final String type;
  final int liters;
  final int price;

  final String description;

  const Product({
    required this.name,
    required this.image,
    required this.tag,
    required this.type,
    required this.liters,
    required this.price,
    required this.description,
  });
}